public class ScriptObservacionVO {
	
	private String nmLog;
	private String cdUsuario;
	private String cdPerson;
	private String feRegistro;
	private String dsObservacion;
	public String getNmLog() {
		return nmLog;
	}
	public void setNmLog(String nmLog) {
		this.nmLog = nmLog;
	}
	public String getCdUsuario() {
		return cdUsuario;
	}
	public void setCdUsuario(String cdUsuario) {
		this.cdUsuario = cdUsuario;
	}
	public String getCdPerson() {
		return cdPerson;
	}
	public void setCdPerson(String cdPerson) {
		this.cdPerson = cdPerson;
	}
	public String getFeRegistro() {
		return feRegistro;
	}
	public void setFeRegistro(String feRegistro) {
		this.feRegistro = feRegistro;
	}
	public String getDsObservacion() {
		return dsObservacion;
	}
	public void setDsObservacion(String dsObservacion) {
		this.dsObservacion = dsObservacion;
	}
	
}
