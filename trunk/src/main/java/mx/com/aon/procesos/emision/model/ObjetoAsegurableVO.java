public class ObjetoAsegurableVO {
	private String inciso;
	private String descripcion;
	private String nmsituac;
	private String status;
	private String cdtipsit; 
	private String cdcia;
	/**
	 * @return the inciso
	 */
	public String getInciso() {
		return inciso;
	}
	/**
	 * @param inciso the inciso to set
	 */
	public void setInciso(String inciso) {
		this.inciso = inciso;
	}
	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}
	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	/**
	 * @return the nmsituac
	 */
	public String getNmsituac() {
		return nmsituac;
	}
	/**
	 * @param nmsituac the nmsituac to set
	 */
	public void setNmsituac(String nmsituac) {
		this.nmsituac = nmsituac;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @return the cdtipsit
	 */
	public String getCdtipsit() {
		return cdtipsit;
	}
	/**
	 * @param cdtipsit the cdtipsit to set
	 */
	public void setCdtipsit(String cdtipsit) {
		this.cdtipsit = cdtipsit;
	}
	public String getCdcia() {
		return cdcia;
	}
	public void setCdcia(String cdcia) {
		this.cdcia = cdcia;
	}
}
