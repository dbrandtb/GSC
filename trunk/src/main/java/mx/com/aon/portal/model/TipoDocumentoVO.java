package mx.com.aon.portal.model;

public class TipoDocumentoVO {
	private String tipoDocu;
	private String descripcion;
	private String operacion;
	
	private String cdDocXcta;
	private String dsFormato;
	
	public String getTipoDocu() {
		return tipoDocu;
	}
	public void setTipoDocu(String tipoDocu) {
		this.tipoDocu = tipoDocu;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getOperacion() {
		return operacion;
	}
	public void setOperacion(String operacion) {
		this.operacion = operacion;
	}
	public String getCdDocXcta() {
		return cdDocXcta;
	}
	public void setCdDocXcta(String cdDocXcta) {
		this.cdDocXcta = cdDocXcta;
	}
	public String getDsFormato() {
		return dsFormato;
	}
	public void setDsFormato(String dsFormato) {
		this.dsFormato = dsFormato;
	}
	
	

}
