package mx.com.aon.catbo.model;

public class FormatoTimeStampVO {
	
	private int dia;
	private int mes;
	private int anio;
	private int hora;
	private int minuto;
	private int segundo;
	private String amopm;
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public int getHora() {
		return hora;
	}
	public void setHora(int hora) {
		this.hora = hora;
	}
	public int getMinuto() {
		return minuto;
	}
	public void setMinuto(int minuto) {
		this.minuto = minuto;
	}
	public int getSegundo() {
		return segundo;
	}
	public void setSegundo(int segundo) {
		this.segundo = segundo;
	}
	public String getAmopm() {
		return amopm;
	}
	public void setAmopm(String amopm) {
		this.amopm = amopm;
	}
	
	
}
